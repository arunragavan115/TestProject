
public class IncrDecr3 {

	public static void main(String[] args) {
		int a = 20;
		int b = 10;
		int c, d;
		
		c = a++;
		d = ++a;
		
		
		System.out.println(a);
		System.out.println(c);
		System.out.println(d);
		
		c = --b;
		d = b--;
		
		
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
	}

}
