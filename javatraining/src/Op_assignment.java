
public class Op_assignment {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 10;
		int c = 2;
		int d = 9;
		int e = 18;
		
		
		a += 15;					// a += (15+2);
		System.out.println(a);
		
		b -= 15;					// b -=(15-5);
		System.out.println(b);
		
		c *= 15 + 5;				//c *=(15+5);
		System.out.println(c);
		
		d /= 2 + 3;					//d /=(2=3);
		System.out.println(d);
		
		e %= 15 + 2;           		//e %=(15+2); modulus means reminder     
		System.out.println(e);
		
		

	}

}
