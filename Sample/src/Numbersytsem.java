
public class Numbersytsem {

	public static void main(String[] args) {
		
		int a = 10;      //Decimal : 10-based
		int b = 0b10;    //binary : 2-based
		int c = 010;     //octal : 8-based
		int d = 0x10;    // hexa-decimal : 16-based
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		



	}

}


